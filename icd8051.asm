CMD:
CLR RS
CLR RW
SETB EN
MOV DAT, A
CLR EN
ACALL DELAY
RET

DISPLAY:
SETB RS
CLR RW
SETB EN
MOV DAT, A
CLR EN
ACALL DELAY
RET

DELAY:MOV R2, #05H
T:MOV R3, #0FFH
Q:MOV R4, #0FFH
R:NOP
DJNZ R4, R
DJNZ R3, Q
DJNZ R2, T
RET

